public class OpEx2 {

	public static void main(String[] args) {
		int x = -10;
		System.out.println("x = " + +x);
		System.out.println("x = " + -x);
		x = 10;
		System.out.println("x = " + +x);
		System.out.println("x = " + -x);
		
		boolean b = true;
		System.out.println("b = " + !b);;
		System.out.println("3 != 2 : " + (3 != 2));
	}
}
