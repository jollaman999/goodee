package chap3;

public class OpEx3 {
	public static void main(String args[]) {
		System.out.println(10%8);
		System.out.println(-10%8);
		System.out.println(10%-8);
		System.out.println(-10%-8);
		System.out.println(10/8);
		System.out.println(-10/8);
		System.out.println(10/-8);
		System.out.println(-10/-8);
	}
}
